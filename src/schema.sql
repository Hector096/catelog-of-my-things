CREATE DATABASE MY_CATALOG


CREATE TABLE GENRE (
    ID SERIAL,
	NAME VARCHAR(50),
	PRIMARY KEY(ID)
	)

    CREATE TABLE LABEL (
    ID SERIAL,
	TITLE VARCHAR(50),
	COLOR VARCHAR(50),
	PRIMARY KEY(ID)
	)

    CREATE TABLE AUTHOR (
    ID SERIAL,
	FIRST_NAME VARCHAR(50),
	LAST_NAME VARCHAR(50),
	PRIMARY KEY(ID)
	)
